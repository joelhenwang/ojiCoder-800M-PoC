def canonicalize_language(language: str) -> str:
    normalized = language.strip().replace("-", "_").replace("/", " ").lower()
    normalized = " ".join(normalized.split())
    normalized = normalized.replace(" ", "_")
    return LANGUAGE_ALIASES.get(normalized, language.strip())


def language_is_selected(language: str, selected_languages: tuple[str, ...]) -> bool:
    return canonicalize_language(language) in set(selected_languages)
